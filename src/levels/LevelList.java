	static int[] tokenize(String s, int numtokens, boolean end)
	{
		int counter = 0;
		if(end)
		{
			numtokens++;
			counter++;
		}
		int[] tokens = new int[numtokens];
		int lastCount = 0;
		int i;
		for(i = 0; i < s.length() && counter < numtokens; i++)
		{
			if(s.charAt(i) == ',')
			{
				tokens[counter] = Integer.parseInt(s.substring(lastCount, i));
				counter++;
				lastCount = i + 1;
			}
			else if(i == s.length() - 1)
			{
				tokens[counter] = Integer.parseInt(s.substring(lastCount));
				counter++;
			}
		}
		if(end)
		{
			tokens[0] = i;
		}
		return tokens;
	}

	static int[] tokenize(String s, int numtokens)
	{
		return tokenize(s, numtokens, false);
	}

